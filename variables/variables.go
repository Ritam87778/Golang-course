package main

import "fmt"

func main() {

	//strings

	var name string = "John Doe"
	

	fmt.Println(name)

	//integers

	var age int = 42

	fmt.Println(age)

	//floats

	var temperature float64 = 20.5

	fmt.Println(temperature)

	//booleans

	var isRaining bool = true

	fmt.Println(isRaining)

	// Shorthand syntax

	 name2 := "Ritam"

	 fmt.Println(name2)

	 var name3 string

	 name3 = "Ricky"

	 fmt.Println(name3)
}